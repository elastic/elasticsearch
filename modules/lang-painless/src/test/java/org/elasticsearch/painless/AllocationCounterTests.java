package org.elasticsearch.painless;

import org.elasticsearch.painless.spi.PainlessTestScript;

/**
 * Verifies the generated counter mechanics on a tracking-enabled compiled script: the {@code $allocBytes} field is wired to
 * the {@code $incAllocBytes}/{@code getAllocBytes} overrides and is reset at every {@code execute} entry. Nothing charges the
 * counter from generated bytecode yet (pre-checks arrive in later PRs), so it is driven here through the interface methods.
 */
public class AllocationCounterTests extends AllocationTestCase {

    public void testIncrementAndRead() {
        PainlessScript script = (PainlessScript) compile("return 1;", "1mb");
        assertEquals(0L, script.getAllocBytes());
        assertEquals(100L, script.$incAllocBytes(100L));
        assertEquals(130L, script.$incAllocBytes(30L));
        assertEquals(130L, script.getAllocBytes());
    }

    public void testExecuteResetsCounter() {
        // The script returns a constant-pool string so nothing is allocated after the reset (returning an int would autobox).
        PainlessTestScript script = compile("return \"x\";", "1mb");
        PainlessScript tracked = (PainlessScript) script;
        tracked.$incAllocBytes(500L);
        assertEquals(500L, tracked.getAllocBytes());

        script.execute(); // reset fires at the execute entry

        assertEquals(0L, tracked.getAllocBytes());
    }

    public void testTrackingDisabledKeepsInterfaceDefaults() {
        // With the limit off the generated class does not override the counter methods; the interface defaults apply.
        PainlessScript script = (PainlessScript) compile("return 1;", "-1b");
        assertEquals(0L, script.getAllocBytes());
        expectThrows(UnsupportedOperationException.class, () -> script.$incAllocBytes(10L));
    }
}

package org.elasticsearch.painless;

import org.elasticsearch.common.settings.Settings;
import org.elasticsearch.painless.spi.PainlessTestScript;
import org.elasticsearch.painless.spi.Whitelist;
import org.elasticsearch.painless.spi.WhitelistLoader;
import org.elasticsearch.script.ScriptContext;
import org.elasticsearch.test.ESTestCase;

import java.util.ArrayList;
import java.util.HashMap;
import java.util.List;
import java.util.Map;

/**
 * Verifies the generated counter mechanics on a tracking-enabled compiled script: the {@code $allocBytes} field is wired to
 * the {@code $incAllocBytes}/{@code getAllocBytes} overrides and is reset at every {@code execute} entry. Nothing charges the
 * counter from generated bytecode yet (pre-checks arrive in later PRs), so it is driven here through the interface methods.
 */
public class AllocationCounterTests extends ESTestCase {

    private static final String LIMIT_KEY = "script.painless.max_allocation_bytes.context." + PainlessTestScript.CONTEXT.name + ".limit";

    /** Compiles {@code source} for the test context with the given limit and returns a fresh script instance. */
    private static PainlessTestScript compile(String source, String limit) {
        Settings settings = Settings.builder().put(LIMIT_KEY, limit).build();
        PainlessScriptEngine engine = new PainlessScriptEngine(settings, scriptContexts());
        PainlessTestScript.Factory factory = engine.compile("test", source, PainlessTestScript.CONTEXT, Map.of());
        return factory.newInstance(Map.of());
    }

    public void testIncrementAndRead() {
        PainlessScript script = (PainlessScript) compile("return 1;", "1mb");
        assertEquals(0L, script.getAllocBytes());
        assertEquals(100L, script.$incAllocBytes(100L));
        assertEquals(130L, script.$incAllocBytes(30L));
        assertEquals(130L, script.getAllocBytes());
    }

    public void testExecuteResetsCounter() {
        PainlessTestScript script = compile("return 1;", "1mb");
        PainlessScript tracked = (PainlessScript) script;
        tracked.$incAllocBytes(500L);
        assertEquals(500L, tracked.getAllocBytes());

        script.execute(); // reset fires at the execute entry

        assertEquals(0L, tracked.getAllocBytes());
    }

    public void testTrackingDisabledKeepsInterfaceDefaults() {
        // With the limit off the generated class does not override the counter methods; the interface defaults apply.
        PainlessScript script = (PainlessScript) compile("return 1;", "-1b");
        assertEquals(0L, script.getAllocBytes());
        expectThrows(UnsupportedOperationException.class, () -> script.$incAllocBytes(10L));
    }

    private static Map<ScriptContext<?>, List<Whitelist>> scriptContexts() {
        Map<ScriptContext<?>, List<Whitelist>> contexts = new HashMap<>();
        List<Whitelist> whitelists = new ArrayList<>(PainlessPlugin.baseWhiteList());
        whitelists.add(WhitelistLoader.loadFromResourceFiles(PainlessPlugin.class, "org.elasticsearch.painless.test"));
        contexts.put(PainlessTestScript.CONTEXT, whitelists);
        return contexts;
    }
}

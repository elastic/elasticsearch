package org.elasticsearch.xpack.esql.qa.ndjson;

import com.carrotsearch.randomizedtesting.annotations.ParametersFactory;
import com.carrotsearch.randomizedtesting.annotations.ThreadLeakFilters;

import org.elasticsearch.test.AzureReactorThreadFilter;
import org.elasticsearch.test.TestClustersThreadFilter;
import org.elasticsearch.test.cluster.ElasticsearchCluster;
import org.elasticsearch.xpack.esql.CsvSpecReader.CsvTestCase;
import org.elasticsearch.xpack.esql.qa.rest.AbstractExternalSourceSpecTestCase;
import org.junit.ClassRule;

import java.io.IOException;
import java.util.List;
import java.util.Set;

/**
 * Parameterized integration tests for standalone NDJSON files.
 * Each csv-spec test is run against every configured storage backend (S3, HTTP, LOCAL).
 */
@ThreadLeakFilters(filters = { TestClustersThreadFilter.class, AzureReactorThreadFilter.class })
public class NdJsonFormatSpecIT extends AbstractExternalSourceSpecTestCase {

    @ClassRule
    public static ElasticsearchCluster cluster = Clusters.testCluster(() -> s3Fixture.getAddress());

    /**
     * Multi-file NDJSON tests that hit a known engine limitation in
     * {@code SchemaAdaptingIterator}: when the query needs no data columns (pure {@code COUNT(*)}
     * or {@code _file.*} metadata only) the constructor rejects the combination with
     * "output schema size [0] does not match mapping width [N]; callers must narrow attributes to
     * data columns only". STRICT resolution always routes through the schema-adapter as well, so all
     * STRICT multi-file variants fail for the same reason. Other formats (CSV/TSV with typed headers,
     * Parquet/ORC with embedded schemas) take a different code path on these queries.
     * <p>
     * The corresponding multi-file tests that <em>do</em> project data columns pass, so the fixtures
     * generated by {@code generateMultifileSplitNdjson_employees} still exercise the format end-to-end
     * for FFW and UBN.
     */
    private static final Set<String> SKIPPED_TESTS = Set.of(
        // STRICT-on-multifile: always exercises the schema-adapter
        "strictCount",
        "strictFilterAndSort",
        "strictSalaryStats",
        "strictAggregateByGender",
        // No data-column projection: empty output schema vs. inferred mapping width
        "ubnCount",
        "ubnExplicitCount",
        "readAllEmployeesMultiFile",
        "multiFileDistinctFileCount",
        "multiFileGroupByFile",
        "multiFileMetadataSizePositive"
    );

    public NdJsonFormatSpecIT(
        String fileName,
        String groupName,
        String testName,
        Integer lineNumber,
        CsvTestCase testCase,
        String instructions,
        StorageBackend storageBackend
    ) {
        super(fileName, groupName, testName, lineNumber, testCase, instructions, storageBackend, "ndjson");
    }

    @Override
    protected String getTestRestCluster() {
        return cluster.getHttpAddresses();
    }

    @Override
    protected void shouldSkipTest(String testName) throws IOException {
        if (SKIPPED_TESTS.contains(testName)) {
            assumeTrue(testName + " not supported by NDJSON multi-file path (SchemaAdaptingIterator limitation)", false);
        }
        super.shouldSkipTest(testName);
    }

    @ParametersFactory(argumentFormatting = "csv-spec:%2$s.%3$s [%7$s]")
    public static List<Object[]> readScriptSpec() throws Exception {
        return readExternalSpecTests("/external-basic.csv-spec", "/external-multifile.csv-spec", "/external-multifile-resolution.csv-spec");
    }
}
